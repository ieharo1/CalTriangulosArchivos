    private void ordenar(double[] x) {
        int i, j, n=x.length;
        double z;
        for (i=0; i < n; i++) {
            for(j=i+1;j<n;j++){
                if(x[i]>x[j]){
                    z=x[i];
                    x[i]=x[j];
                    x[j]=z;
                }
            }
        }
    }
