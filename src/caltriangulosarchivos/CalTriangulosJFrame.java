    private void btn10ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn10ActionPerformed
        // TODO add your handling code here:
        JFileChooser chooser = new JFileChooser();
        // Establecer el directorio de trabajo como
        // el directorio por defecto
        chooser.setCurrentDirectory(new File("."));
        //Establecer un fichero por defecto
        //abrir la ventana de dialogo
        int resultado = chooser.showOpenDialog(this);
        if (resultado == JFileChooser.APPROVE_OPTION) {
            File f = chooser.getSelectedFile();
            try {
                double [] a = new double[3];
                double per,s,sup;
                BufferedReader br = new BufferedReader(new FileReader(f));
                String linea;
                while ((linea = br.readLine()) != null){
                    a[0] =Double.parseDouble(linea);
                    if((linea=br.readLine())==null){
                        break;
                    }
                    a[1]=Double.parseDouble(linea);
                    if((linea=br.readLine())==null){
                        break;
                    }
                    a[2]=Double.parseDouble(linea);
                    getModelo().addElement("Lado 1 = "+a[0]+" Lado 2 = "+a[1]+" Lado 3 = "+a[2]);
                    ordenar(a);
                    if((a[0]+a[1])>a[2]){
                        per=a[0]+a[1]+a[2];
                        s= per/2;
                        sup= s;
                        for (int i = 0; i < 3; i++) {
                            sup*=(s-a[i]);
                        }
                        sup = Math.sqrt(sup);
                        getModelo().addElement("Perimetro = "+per+"Superficie = "+sup);
                        if(a[0]==a[2]){
                           getModelo().addElement("Es un tringulo equilatero");

                        }
                        else if(a[1]==a[2]){
                            getModelo().addElement(" Es un trianguloisoceles" );
                        }
                        else {
                            getModelo().addElement(" Es un escaleno" );
                        }
                    }
                    else{
                        getModelo().addElement("No es un triangulo" );
                    }
                
                // Bloque de lectura proceso;
                getLista().setModel(getModelo());
                }
            }
            catch (IOException e) {
                JOptionPane.showMessageDialog(null, "Error al abrir el archivo " + f.getName());
            }
        }
    }//GEN-LAST:event_btn10ActionPerformed

